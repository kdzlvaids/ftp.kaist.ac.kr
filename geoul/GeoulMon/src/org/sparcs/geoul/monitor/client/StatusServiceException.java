package org.sparcs.geoul.monitor.client;

@SuppressWarnings("serial")
public class StatusServiceException extends Exception {

	public StatusServiceException() {
		super();
	}

	public StatusServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public StatusServiceException(String message) {
		super(message);
	}

	public StatusServiceException(Throwable cause) {
		super(cause);
	}

}
